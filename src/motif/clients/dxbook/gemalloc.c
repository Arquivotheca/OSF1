#include <ssdef.h>
#endif


extern char *calloc();

char *
geMalloc(b)
int		b;
{                                                                         
static char	*p, *error_string;

p = NULL;

if ((p = calloc(b, 1))) return(p);

error_string = (char *) geFetchLiteral("GE_ERR_MALLOC", MrmRtypeChar8);
if (error_string != NULL) 
  {geError(error_string, TRUE);
   XtFree(error_string);
 }
return(NULL);
}
