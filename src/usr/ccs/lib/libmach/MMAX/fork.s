#ifndef lint

#endif not lint

#include "SYS.h"

	.globl	_mach_init

SYSCALL(fork)
	cmpqd	$0,r1	# parent since r1 == 0 in parent, 1 in child
	beq	.Lparent
	jsr	@_mach_init
	movqd	$0,r0
.Lparent:
	ret	$0	# pid = fork()
