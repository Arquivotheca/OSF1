#ifndef lint

#endif not lint

#define _NAME_SPACE_WEAK_STRONG
#include <alpha/regdef.h>
#include <alpha/asm.h>
#include <syscall.h>

FRMSIZE = 16
IMPORT(task_self_, 8)

NESTED(fork, FRMSIZE, ra)
	ldgp    gp, 0(pv)
	lda	sp, -FRMSIZE(sp)	# need some temp stack space
	stq	ra, (sp)

  ldiq	v0, SYS_fork
  CHMK()
  bne	a3, err
	beq	a4, parent
	stq zero, task_self_
	CALL(mach_init)
  ldiq	v0, 0

parent:
	ldq	ra, (sp)
	lda	sp, FRMSIZE(sp)		# restore sp..
	RET				# pid = fork()

err:
	lda	sp, FRMSIZE(sp)		# restore sp..
  jmp     zero, _cerror

.end fork
