/*
 * NAME: 	AFclose
 * FUNCTION:	Close an Attribute File.
 * RETURN VALUE DESCRIPTION: void
 */

#include <stdio.h>
#include <AFdefs.h>

void
AFclose( AFILE_t af )
{
	fclose(af->AF_iop);

	free((void *)af->AF_cent.EN_cbuf);
	free((void *)af->AF_cent.EN_catr);

	free((void *)af->AF_dent.EN_cbuf);
	free((void *)af->AF_dent.EN_catr);

	free((void *)af);
}
