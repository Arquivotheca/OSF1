# include	"cursesext.h"

/*
 * NAME:        waddstr
 *
 * FUNCTION:
 *
 *      This routine adds a wchar_t sstring starting at (_cury,_curx)
 *
 */

extern	int	waddnwstr( WINDOW *win, wchar_t *wstr, int n )
{
# ifdef DEBUG
	if(outf)
	{
		if( win == stdscr )
		{
			fprintf(outf, "WADDWSTR(stdscr, ");
		}
		else
		{
			fprintf(outf, "WADDWSTR(%o, ", win);
		}
		fprintf(outf, "\"%ws\")\n", wstr);
	}
# endif	/* DEBUG */
	while( (n == INFINI || n-- ) && *wstr )
	{
		if( waddwch( win, ( chtype )*wstr++ ) == ERR )
		{
			return ERR;
		}
	}
	return OK;
}
