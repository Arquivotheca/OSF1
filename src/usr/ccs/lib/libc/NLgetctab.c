#define MSGSTR(msg,def) 	 NLgetamsg(MF_LIBC, MS_LIBC,msg,def)
					      
extern struct NLctab *_NLctab;
extern struct NLcoldesc *_NLcoldesc;
extern long shget();
extern char *shmat();
extern char *NLgetenv();
extern void exit();
extern char *sbrk();

void NLgetctab(nlfile)
char *nlfile;
{
	int saverrno = errno;
	char *ctabf = (nlfile) ? nlfile : NLgetenv("NLCTAB");
        char *addr;
	int key;
	int _NLcfile;
	struct stat buf;
	static unsigned long sbrksiz = 0;
	static char *sbrkreg;
	static short usemalloc = 0;
	static short pastfirst = 0;

	/*
	 * Free space only when malloc() was called
	 * initially.
	 */

	if ( usemalloc ) {
	    free (_NLctab);
	    usemalloc = 0;
	}

    /* if null pointer or string, get pointer to shared memory for
       default ctab */

	if (ctabf == NULL || *ctabf == '\0') {
retry:
		errno = saverrno;
		if ((_NLcfile = open(DEFCTAB,0,O_RDONLY)) < 0) {
		    _NLctab = (struct NLctab *)tempdeftab;/* default coll. tbl*/
		    return;
		}
		if (fstat(_NLcfile, &buf) != 0) {
		    fprintf(stderr,MSGSTR(NO_FSTAT,"fstat of default NLCTAB file failed\n"));
		    (void)close (_NLcfile);
		    _NLctab = NULL;	/* no default file, safe value */
		    return;
		}
		(void)close (_NLcfile);
		if ((key = shmget (NLCTAB_KEY, buf.st_size, IPC_EXCL|S_IROTH)) != -1) {
			if ((addr = shmat(key, 0, SHM_RDONLY)) != (char*)-1) {
			    _NLctab = (struct NLctab *)addr;
			}
		}
		ctabf = DEFCTAB;
	}
	if ((_NLcfile = open(ctabf, O_RDONLY, 0)) < 0)
		goto retry;
	if (fstat(_NLcfile, &buf) != 0)
		goto retry;
	/*
	 * Allocate space for the ctab table.  If this is the first call to
	 * NLgetctab(), i.e. from the startup code, then we must use sbrk()
	 * rather than malloc() because some programs (/bin/sh) have their own
	 * malloc() which won't work until initialized from main().  If this
	 * is not the first call, then we reuse the sbrk space if it is large
	 * enough, otherwise we call malloc().
	 */
	if (!pastfirst) {
		sbrksiz =  (buf.st_size + 3) & ~3;
		sbrkreg =  sbrk((int)sbrksiz);
		_NLctab = (struct NLctab*)sbrkreg;
		pastfirst = 1;
	}
	else if (buf.st_size > sbrksiz) {
	        _NLctab = (struct NLctab*)malloc((size_t)buf.st_size);
		usemalloc++;
	}
	else {
		_NLctab = (struct NLctab*)sbrkreg;
	}
	if (read(_NLcfile, _NLctab, (unsigned)buf.st_size) != buf.st_size) {
		fprintf(stderr,"read of ctab failed\n");
		return;
	}
	if (_NLctab->ct_mag0 != NLCTMAG0 || _NLctab->ct_mag1 != NLCTMAG1) {
		fprintf(stderr,": Bad magic number... not ctab file\n");
		return;
	}
	(void)close(_NLcfile);
	_NLcoldesc = (struct NLcoldesc *)((char *)_NLctab + sizeof (*_NLctab));
	return;
}
