/* abs:		Absolute value of an int.
 * Calling sequence:	absval = abs (val);
 * Returns: absolute value of val
 * NOTE: Absolute value of most negative int (0x80000000) is itself, as there
 *	 is no positive 2's complement number to match it in 32 bits.
 */
#include "SYS.h"

	.file	"abs.s"
	.text
	.align	1
ENTRY(abs)
	absd	SP(4),r0
	EXIT
	ret	$0
