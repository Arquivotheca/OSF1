#include <alpha/regdef.h>
#include <alpha/asm.h>
#include <alpha/pal.h>

/*
 * Read the system cycle counter
 */
LEAF(scc)
	bis s0,zero,t1
	bis s1,zero,t2
	bis ra,zero,t0			# save ra
	call_pal PAL_rd_scc
	bis ra,zero,v0
	bis t1,zero,s0
	bis t2,zero,s1
	RET
END(scc)

