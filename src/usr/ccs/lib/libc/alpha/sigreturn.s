#include <alpha/regdef.h>
#include <alpha/asm.h>
#include <syscall.h>
#ifdef PROF
#undef LEAF
#define	LEAF(x)						\
	.globl	x;					\
	.ent	x,0;					\
x:;							\
	.frame	sp,16,ra				\
	lda	sp,-16(sp)				\
	stq	at,0(sp)
#endif

SYSCALL(sigreturn)
#ifdef PROF
	ldq	at,0(sp)
	lda	sp,16(sp)
#endif
	RET
END(sigreturn)
