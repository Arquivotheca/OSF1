/* hostorder = ntohs(netorder) */

#include <mips/regdef.h>
#include <mips/asm.h>

LEAF(ntohs)
#ifdef MIPSEL
	# start with ab
	sll	v0,a0,8			# ?ab0
	srl	v1,a0,8			# 0??a
	and	v1,0xff			# 000a
	or	v0,v1			# ??ba
	and	v0,0xffff		# 00ba
#else
	and	v0,a0,0xffff
#endif
	j	ra
.end ntohs
