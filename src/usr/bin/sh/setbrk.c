#include	"defs.h"

char 	*sbrk();

uchar_t *
setbrk(incr)
{

	register uchar_t *a = (uchar_t *)sbrk(incr);

	if((int) a == -1) 
	{ 
# if DEBUG
		error("AARGH! setbrk failed");
		abort();
# endif
		error (MSGSTR(M_NOSPACE,(char *)nospace));
	}
	else
		brkend = a + incr;
	return(a);
}
