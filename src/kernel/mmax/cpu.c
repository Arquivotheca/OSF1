/*
 *	Machine-dependent cpu control routines.
 *
 *	Nothing happens on multimax.
 */

#include <mach/kern_return.h>

kern_return_t cpu_start(slot_num)
int	slot_num;
{
#ifdef	lint
	slot_num++;
#endif	lint
	return(KERN_FAILURE);
}

kern_return_t cpu_control(slot_num, info, count)
int	slot_num, *info, count;
{
#ifdef	lint
	slot_num++;
	info++;
	count++;
#endif	lint
	return (KERN_FAILURE);
}
