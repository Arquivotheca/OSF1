#include <stdio.h>
#include <sys/errno.h>
extern char *sys_errlist[];
extern int sys_nerr;
extern int errno;

char *progname = "";

int
report_error(file, operation)
FILE	*file;
char	*operation;
{
int	error = errno;

	fprintf(stderr,"%s: %s failed: %s (errno %d).\n",
		progname, operation,
		(error<sys_nerr)?sys_errlist[error]:"Unknown error", error);
	return(error);
}
